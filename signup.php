// Redirect if already logged in
if (isset($_SESSION['user_id'])) {
    header('Location: dashboard.php');
    exit();
}

$error = '';
$success = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = sanitizeInput($_POST['username'] ?? '');
    $email = sanitizeInput($_POST['email'] ?? '');
    $password = $_POST['password'] ?? '';
    $confirm_password = $_POST['confirm_password'] ?? '';
    $csrf_token = $_POST['csrf_token'] ?? '';

    // Validate CSRF token
    if (!validateCSRFToken($csrf_token)) {
        $error = 'Security validation failed. Please try again.';
    } elseif (empty($username) || empty($email) || empty($password)) {
        $error = 'All fields are required.';
    } elseif (!filter_var($email, FILTER_VALIDATE_EMAIL)) {
        $error = 'Please enter a valid email address.';
    } elseif (strlen($password) < 8) {
        $error = 'Password must be at least 8 characters long.';
    } elseif ($password !== $confirm_password) {
        $error = 'Passwords do not match.';
    } else {
        try {
            $pdo = getDBConnection();
            
            // Check if username or email already exists - UPDATED column names
            $stmt = $pdo->prepare("SELECT user_id FROM users WHERE username = ? OR email = ?");
            $stmt->execute([$username, $email]);
            
            if ($stmt->fetch()) {
                $error = 'Username or email already exists.';
            } else {
                // Create new user - UPDATED to match your table structure
                $hashed_password = password_hash($password, PASSWORD_DEFAULT);
                $user_type = 'customer'; // Default user type
                
                $stmt = $pdo->prepare("INSERT INTO users (username, email, password, created_at, user_type) VALUES (?, ?, ?, NOW(), ?)");
                $stmt->execute([$username, $email, $hashed_password, $user_type]);
                
                $success = 'Account created successfully! You can now login.';
                $_POST = []; // Clear form
            }
        } catch (PDOException $e) {
            error_log("Signup error: " . $e->getMessage());
            $error = 'Registration failed. Please try again.';
        }
    }
}

